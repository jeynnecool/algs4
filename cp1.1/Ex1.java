public class Ex1
{
    public static void main(String[] args)
    {
        System.out.println((0 + 15) / 2); // 7
        System.out.println(2 * Math.pow(10, -6) * 100000000.1); // 2.0E-6 = 2.0 x 10^(-6); output 200.0000002
        System.out.println(true && true || true && true); // true
    }
}